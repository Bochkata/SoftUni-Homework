using System;

namespace T06ReversedChars
{
    class Program
    {
        static void Main(string[] args)
        {
            char n1 = char.Parse(Console.ReadLine());
            char n2 = char.Parse(Console.ReadLine());
            char n3 = char.Parse(Console.ReadLine());
            Console.WriteLine("{0} {1} {2}", n3, n2, n1);
        }
    }
}
